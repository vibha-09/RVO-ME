"""
run_evaluation.py — master evaluation pipeline for research paper.

Runs segmentation evaluation (+ optional detection), then qualitative
visualization and a combined LaTeX report covering both tasks.

Usage
-----
  cd backend
  python -m evaluation.run_evaluation \\
      --dataset-root  ../dataset/RVO-Lesion \\
      --seg-weights   weights/attention_unet.pth \\
      --det-weights   weights/hf_frcnn.pt \\
      --output        evaluation/results

Expected dataset layout
-----------------------
  <dataset-root>/
      Image_Seg/
          images/           raw OCT images
          masks/            ground-truth segmentation masks
          train.txt
          test.txt
      RVO_Lesion_Labelme/   (or RVO_Lesion_LabelMe)
          <patient_id>/
              <stem>.json
      detection/
          annotations.json  (optional — produced by convert_labelme_to_coco.py)
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))


# ─────────────────────────────────────────────────────────────────────────────
# Helpers
# ─────────────────────────────────────────────────────────────────────────────

def _find_labelme_dir(root: Path) -> Path | None:
    candidates = [
        "RVO_Lesion_LabelMe", "RVO_Lesion_Labelme",
        "RVO_Lesion_labelme", "rvo_lesion_labelme",
    ]
    for name in candidates:
        p = root / name
        if p.exists():
            return p
    for p in root.iterdir():
        if p.is_dir() and "labelme" in p.name.lower():
            return p
    return None


# ─────────────────────────────────────────────────────────────────────────────
# Combined LaTeX summary
# ─────────────────────────────────────────────────────────────────────────────

def _write_combined_latex(
    seg_metrics: dict,
    det_metrics: dict,
    output_path: Path,
) -> None:
    import numpy as np

    CLASS_NAMES = ["Background", "SRF", "IRF", "ELM", "EZ"]
    hd95_mean   = seg_metrics.get("hd95_mean")
    asd_mean    = seg_metrics.get("asd_mean")
    ci_dice     = seg_metrics.get("ci_dice")
    ci_iou      = seg_metrics.get("ci_iou")
    n_images    = seg_metrics.get("n_images", 0)

    hd_col  = hd95_mean is not None
    asd_col = asd_mean  is not None
    n_data  = 6 + int(hd_col) + int(asd_col)

    def _f(v, d=4):
        try:
            return f"{float(v):.{d}f}" if not (v != v) else "—"
        except Exception:
            return "—"

    def _pct(v):
        try:
            return f"{float(v)*100:.2f}" if not (v != v) else "—"
        except Exception:
            return "—"

    def _pct_ci(mean_v, lo, hi):
        try:
            half = (float(hi) - float(lo)) / 2.0
            return f"{float(mean_v)*100:.2f} $\\pm$ {half*100:.2f}"
        except Exception:
            return _pct(mean_v)

    lines = [
        r"% ============================================================",
        r"% Combined LaTeX tables — auto-generated by run_evaluation.py",
        r"% ============================================================",
        r"",
        r"% ── Table 1: Segmentation ──────────────────────────────────",
        r"\begin{table*}[ht]",
        r"\centering",
        (
            r"\caption{Segmentation performance of the Attention U-Net on the "
            f"RVO-Lesion test set (n\\,=\\,{n_images}). "
            r"Dice and IoU: mean\,\%\,$\pm$\,95\,\%\,CI "
            r"(bootstrap, 1\,000 resamples). "
            r"HD\textsubscript{95}/ASD in pixels; lower is better ($\downarrow$).}"
        ),
        r"\label{tab:seg_results}",
        r"\resizebox{\textwidth}{!}{%",
        r"\begin{tabular}{l" + "c" * n_data + r"}",
        r"\hline",
    ]

    # Column header
    hdr = (r"\textbf{Class} & \textbf{Dice (\%)} & \textbf{IoU (\%)} & "
           r"\textbf{Prec (\%)} & \textbf{Recall (\%)} & \textbf{Spec (\%)} & \textbf{F1 (\%)}")
    if hd_col:  hdr += r" & \textbf{HD\textsubscript{95} (px)}"
    if asd_col: hdr += r" & \textbf{ASD (px)}"
    hdr += r" \\"
    lines += [hdr, r"\hline"]

    for i, name in enumerate(CLASS_NAMES):
        dice_v = seg_metrics["dice"][i]
        iou_v  = seg_metrics["iou"][i]
        if ci_dice and not (dice_v != dice_v):
            lo, hi   = ci_dice[i]
            dice_str = _pct_ci(dice_v, lo, hi)
        else:
            dice_str = _pct(dice_v)
        if ci_iou and not (iou_v != iou_v):
            lo, hi  = ci_iou[i]
            iou_str = _pct_ci(iou_v, lo, hi)
        else:
            iou_str = _pct(iou_v)

        vals = [
            dice_str, iou_str,
            _pct(seg_metrics["precision"][i]),
            _pct(seg_metrics["recall"][i]),
            _pct(seg_metrics["specificity"][i]),
            _pct(seg_metrics["f1"][i]),
        ]
        if hd_col:  vals.append(_f(hd95_mean[i], 2))
        if asd_col: vals.append(_f(asd_mean[i],  2))
        lines.append(f"{name} & " + " & ".join(vals) + r" \\")

    lines += [
        r"\hline",
        f"mDice (excl.\\ BG) & \\multicolumn{{{n_data}}}{{c}}{{{_pct(seg_metrics['mean_dice'])}\\,\\%}} \\\\",
        f"mIoU (excl.\\ BG)  & \\multicolumn{{{n_data}}}{{c}}{{{_pct(seg_metrics['miou'])}\\,\\%}} \\\\",
        f"FW-IoU             & \\multicolumn{{{n_data}}}{{c}}{{{_pct(seg_metrics['fwiou'])}\\,\\%}} \\\\",
        f"Pixel Accuracy     & \\multicolumn{{{n_data}}}{{c}}{{{_pct(seg_metrics['pixel_acc'])}\\,\\%}} \\\\",
        f"Cohen's $\\kappa$  & \\multicolumn{{{n_data}}}{{c}}{{{_f(seg_metrics['kappa'])}}} \\\\",
        r"\hline",
        r"\end{tabular}%",
        r"}",
        r"\end{table*}",
        r"",
    ]

    if det_metrics:
        def _ci_str(lo, hi, d=4):
            try:
                if np.isnan(float(lo)) or np.isnan(float(hi)):
                    return ""
                return r" \scriptsize{[" + f"{float(lo):.{d}f}–{float(hi):.{d}f}" + r"]}"
            except Exception:
                return ""

        FROC_FP_TARGETS = [0.125, 0.25, 0.5, 1.0, 2.0, 4.0, 8.0]
        def _fkey(fp):
            return str(fp).replace(".", "")

        ci_ap50 = det_metrics.get("ci_ap50", (np.nan, np.nan))
        ci_prec = det_metrics.get("ci_precision", (np.nan, np.nan))
        ci_rec  = det_metrics.get("ci_recall",    (np.nan, np.nan))
        ci_f1   = det_metrics.get("ci_f1",        (np.nan, np.nan))
        thr     = det_metrics.get("opt_threshold", 0.5)

        lines += [
            r"% ── Table 2: Detection ─────────────────────────────────────",
            r"\begin{table}[ht]",
            r"\centering",
            r"\caption{HF detection performance of Faster R-CNN on the RVO-Lesion test set. "
            r"Bracketed values: bootstrap 95\,\%\,CI (1\,000 image-level resamples). "
            r"CPM: Competition Performance Metric.}",
            r"\label{tab:det_results}",
            r"\begin{tabular}{lc}",
            r"\hline",
            r"\textbf{Metric} & \textbf{Value} \\",
            r"\hline",
            r"\multicolumn{2}{l}{\textit{COCO-style AP}} \\",
            f"AP @ IoU=0.50               & {_f(det_metrics['ap50'])}{_ci_str(*ci_ap50)} \\\\",
            f"AP @ IoU=0.75               & {_f(det_metrics['ap75'])} \\\\",
            f"mAP @ [.50:.05:.95]         & {_f(det_metrics['map_50_95'])} \\\\",
            f"AP$_{{\\text{{small}}}}$    & {_f(det_metrics['ap_small'])} \\\\",
            r"\hline",
            r"\multicolumn{2}{l}{\textit{Average Recall}} \\",
            f"AR @ 1 det                  & {_f(det_metrics['ar1'])} \\\\",
            f"AR @ 10 dets                & {_f(det_metrics['ar10'])} \\\\",
            f"AR @ 100 dets               & {_f(det_metrics['ar100'])} \\\\",
            r"\hline",
            r"\multicolumn{2}{l}{\textit{At optimal F1 threshold ("
            + f"conf={thr:.3f}" + r")}} \\",
            f"Precision                   & {_f(det_metrics['precision'])}{_ci_str(*ci_prec)} \\\\",
            f"Recall                      & {_f(det_metrics['recall'])}{_ci_str(*ci_rec)} \\\\",
            f"F1 score                    & {_f(det_metrics['f1'])}{_ci_str(*ci_f1)} \\\\",
            r"\hline",
            r"\multicolumn{2}{l}{\textit{FROC analysis}} \\",
            f"CPM                         & {_f(det_metrics.get('cpm', np.nan))} \\\\",
            f"pAUC-FROC [0.125--8 FP/img] & {_f(det_metrics.get('pauc_froc', np.nan))} \\\\",
        ]
        for fp_t in FROC_FP_TARGETS:
            k = f"sens_fp{_fkey(fp_t)}"
            lines.append(f"Sens @ {fp_t}\\,FP/img & {_f(det_metrics.get(k, np.nan))} \\\\")
        lines += [
            r"\hline",
            r"\end{tabular}",
            r"\end{table}",
        ]

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text("\n".join(lines), encoding="utf-8")
    print(f"\n  Combined LaTeX: {output_path}")


# ─────────────────────────────────────────────────────────────────────────────
# Main
# ─────────────────────────────────────────────────────────────────────────────

def run(args: argparse.Namespace) -> None:
    from evaluation.evaluate_segmentation import run_evaluation as run_seg
    from evaluation.evaluate_detection    import run_evaluation as run_det
    from evaluation.visualize_predictions import run_visualization as run_seg_viz
    from evaluation.visualize_detections  import run_visualization as run_det_viz

    root      = args.dataset_root
    image_seg = root / "Image_Seg"
    output    = args.output

    images_dir = image_seg / "images"
    masks_dir  = image_seg / "masks"
    test_txt   = image_seg / "test.txt"

    if not test_txt.exists():
        print(f"ERROR: test.txt not found at {test_txt}")
        print("       Run prepare_splits.py first to generate it.")
        import sys; sys.exit(1)

    # ── Step 1: Segmentation metrics ──────────────────────────────────────────
    print("\n" + "─" * 62)
    print("  STEP 1 / 3  —  Segmentation Evaluation")
    print("─" * 62)

    seg_output = output / "segmentation"

    if not images_dir.exists():
        print(f"WARNING: {images_dir} not found — skipping segmentation.")
        seg_metrics = {}
    else:
        seg_metrics = run_seg(
            images_dir   = images_dir,
            masks_dir    = masks_dir,
            test_txt     = test_txt,
            weights_path = args.seg_weights,
            output_dir   = seg_output,
            compute_hd   = not args.no_hd95,
            n_bootstrap  = args.n_bootstrap,
        )

    # ── Step 2: Detection metrics ─────────────────────────────────────────────
    print("\n" + "─" * 62)
    print("  STEP 2 / 3  —  HF Detection Evaluation")
    print("─" * 62)

    annotations_json = args.annotations
    if annotations_json is None:
        for cand in [root / "detection" / "annotations.json",
                     Path("detection") / "annotations.json"]:
            if cand.exists():
                annotations_json = cand
                break

    labelme_dir = args.labelme_dir or _find_labelme_dir(root)

    det_output = output / "detection"
    if not images_dir.exists():
        det_metrics = {}
    elif annotations_json is None and labelme_dir is None:
        print("WARNING: No GT annotations found — skipping detection.")
        print("         Provide --annotations or ensure RVO_Lesion_Labelme/ exists.")
        det_metrics = {}
    else:
        det_metrics = run_det(
            images_dir       = images_dir,
            test_txt         = test_txt,
            weights_path     = args.det_weights,
            output_dir       = det_output,
            annotations_json = annotations_json,
            labelme_dir      = labelme_dir,
            conf_threshold   = args.conf_threshold,
            max_images       = args.max_images,
            resize_eval      = args.resize_eval,
        )

    # ── Step 3: Qualitative visualizations ────────────────────────────────────
    print("\n" + "─" * 62)
    print("  STEP 3 / 3  —  Qualitative Visualization")
    print("─" * 62)

    if images_dir.exists() and masks_dir.exists():
        for mode in ["random", "best", "worst", "diverse"]:
            run_seg_viz(
                images_dir   = images_dir,
                masks_dir    = masks_dir,
                test_txt     = test_txt,
                weights_path = args.seg_weights,
                output_dir   = seg_output,
                n_samples    = args.n_qual_samples,
                mode         = mode,
                seed         = 42,
            )

    if images_dir.exists() and (annotations_json or labelme_dir):
        for mode in ["random", "best", "worst", "fp_heavy"]:
            run_det_viz(
                images_dir       = images_dir,
                test_txt         = test_txt,
                weights_path     = args.det_weights,
                output_dir       = det_output,
                annotations_json = annotations_json,
                labelme_dir      = labelme_dir,
                conf_threshold   = max(args.conf_threshold, 0.50),
                n_samples        = args.n_qual_samples,
                mode             = mode,
                seed             = 42,
            )

    # ── Combined LaTeX ────────────────────────────────────────────────────────
    if seg_metrics:
        _write_combined_latex(
            seg_metrics = seg_metrics,
            det_metrics = det_metrics,
            output_path = output / "combined_latex_tables.tex",
        )

    print(f"\n  All outputs written to: {output}")
    print("  " + "─" * 30)


# ─────────────────────────────────────────────────────────────────────────────
# CLI
# ─────────────────────────────────────────────────────────────────────────────

def _parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description="Full evaluation pipeline (segmentation + detection + visualization)"
    )
    p.add_argument("--dataset-root",   required=True, type=Path,
                   help="Root directory containing Image_Seg/ and LabelMe folder")
    p.add_argument("--seg-weights",    default=None,  type=Path,
                   help="Path to attention_unet.pth")
    p.add_argument("--det-weights",    default=None,  type=Path,
                   help="Path to hf_frcnn.pt")
    p.add_argument("--annotations",    default=None,  type=Path,
                   help="COCO annotations.json for detection GT (optional)")
    p.add_argument("--labelme-dir",    default=None,  type=Path,
                   help="LabelMe JSON directory (detection GT fallback)")
    p.add_argument("--conf-threshold", default=0.05,  type=float,
                   help="Min confidence for detection (default 0.05)")
    p.add_argument("--output",         default=Path("evaluation/results"), type=Path,
                   help="Root output directory")
    p.add_argument("--no-hd95",        action="store_true",
                   help="Skip surface distance metrics (faster)")
    p.add_argument("--n-bootstrap",    default=1000,  type=int,
                   help="Bootstrap resamples for CI (default 1000)")
    p.add_argument("--n-qual-samples", default=8,     type=int,
                   help="Rows per qualitative grid (default 8)")
    p.add_argument("--max-images",     default=0,     type=int,
                   help="Limit detection eval to first N images (0 = all). "
                        "Use --max-images 5 to do a quick smoke-test.")
    p.add_argument("--resize-eval",    default=0,     type=int,
                   help="Pre-resize images (long side ≤ N px) before detection inference. "
                        "0 = no resize. E.g. --resize-eval 800 gives 3-5x CPU speedup.")
    return p.parse_args()


if __name__ == "__main__":
    run(_parse_args())
